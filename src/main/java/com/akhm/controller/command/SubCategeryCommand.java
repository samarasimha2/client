package com.akhm.controller.command;

public class SubCategeryCommand {
	private String subCategeryName;
	private String subCategeryDescription;
	private String subCategeryCode;
	private Integer categeryCode;
	public String getSubCategeryName() {
		return subCategeryName;
	}
	public void setSubCategeryName(String subCategeryName) {
		this.subCategeryName = subCategeryName;
	}
	public String getSubCategeryDescription() {
		return subCategeryDescription;
	}
	public void setSubCategeryDescription(String subCategeryDescription) {
		this.subCategeryDescription = subCategeryDescription;
	}
	public String getSubCategeryCode() {
		return subCategeryCode;
	}
	public void setSubCategeryCode(String subCategeryCode) {
		this.subCategeryCode = subCategeryCode;
	}
	public Integer getCategeryCode() {
		return categeryCode;
	}
	public void setCategeryCode(Integer categeryCode) {
		this.categeryCode = categeryCode;
	}

}
