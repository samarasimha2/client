package com.akhm.controller.command;

public class CategeryCommand {
	private String categeryName;
	private String categeryDescription;
	private String categeryCode;
	public String getCategeryName() {
		return categeryName;
	}
	public void setCategeryName(String categeryName) {
		this.categeryName = categeryName;
	}
	public String getCategeryDescription() {
		return categeryDescription;
	}
	public void setCategeryDescription(String categeryDescription) {
		this.categeryDescription = categeryDescription;
	}
	public String getCategeryCode() {
		return categeryCode;
	}
	public void setCategeryCode(String categeryCode) {
		this.categeryCode = categeryCode;
	}

}
